// Math.log2: 數值取底數為 2 的對數
Math.log2(1024)
// <- 10


// 向下相容 Math.log2
function mathLog2 (value) {
  return Math.log(value) / Math.LN2
}

// 向下相容的版本精準度較差
Math.log2(1 << 29) // 原生版本
// <- 29
mathLog2(1 << 29) // 向下相容版本
// <- 29.000000000000004


// << 運算子: 位元向左移動
0b000001 // <- 1
0b000001 << 2 // <- 向左移動兩個位置
0b000100 // <- 4

0b00001101 // <- 13
0b00001101 << 4 // <- 向左移動四個位置
0b11010000 // <- 208
